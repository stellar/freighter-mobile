export interface IconProps {
  name?: keyof typeof Icons;
  circle?: boolean;
  size?: number;
  color?: string;
  testID?: string;
  onPress?: () => void;
}

const CircleContainer = styled.View`
  width: ${({ size }: { size: number }) => px(size * 1.5)};
  height: ${({ size }: { size: number }) => px(size * 1.5)};
  border-radius: 50%;
  background-color: ${THEME.colors.background.default};
  border-color: ${THEME.colors.border.default};
  border-width: 1px;
  display: flex;
  align-items: center;
  justify-content: center;
`;

const IconComponent: React.FC<IconProps> = ({
  name,
  size = 24,
  color = THEME.colors.primary,
  circle,
  testID,
  onPress,
}) => {
  const IconSvg = name ? Icons[name] : null;

  if (!IconSvg) {
    return null;
  }

  const iconElement = (
    <IconSvg
      width={px(size)}
      height={px(size)}
      color={color}
      testID={testID}
      onPress={onPress}
    />
  );

  if (circle) {
    return (
      <CircleContainer size={size} onPress={onPress}>
        {iconElement}
      </CircleContainer>
    );
  }

  return iconElement;
};

type IconType = {
  [K in keyof typeof Icons]: React.FC<Omit<IconProps, "name">>;
} & {
  Component: typeof IconComponent;
};

// Create individual icon components
const Icon = (Object.keys(Icons) as Array<keyof typeof Icons>).reduce(
  (acc, key) => {
    // eslint-disable-next-line no-param-reassign
    acc[key] = (props: Omit<IconProps, "name">) => (
      <IconComponent name={key} {...props} />
    );
    return acc;
  },
  {} as IconType,
);

// Add the base component to the Icon object
Icon.Component = IconComponent;

export default Icon;
